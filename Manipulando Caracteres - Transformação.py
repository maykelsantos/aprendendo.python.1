frase = ' Jesus Te Ama'
print (frase.replace('Jesus', 'Deus'))
# A função 'replace' substitui os caracteres espeficificados entre aspas por outros caracteres espeficicados entre aspas
print (frase.upper())
# A função 'upper' transforma todos os caracteres da variável em MAIÚSCULOS
print (frase.lower())
# A função 'lower' transforma todos os caracteres da variável em minúsculos
print (frase.capitalize())
# A função 'capitalize' transforma todos os caracteres da variável em minúsculos EXCETO o primeiro
print (frase.title())
# A função 'title' transforma o primeiro caractere em letra MAIÚSCULA
print (frase.strip())
# A função 'strip' remove todos os espaços inúteis no início da variável e no seu fim
print (frase.rstrip())
print (frase.lstrip())
# As funções 'rstrip' ou 'lstrip' removem os espaçoes inúteis do lado direito ou esquerdo da variável, respectivamente
