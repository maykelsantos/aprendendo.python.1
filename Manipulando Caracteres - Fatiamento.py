# cada caractere é representado por um número apartir do zero inclusive os espaços
frase = 'Jesus Te Ama'
print (frase[0:5])
print (frase[6:8])
print (frase[9:12])
print (frase[0:12:2]) # Informa os caracteres de 0 à 12 de 2 em 2
print (frase[:5]) # Quando existir omissão de número automaticamente substitui a omissão pelo número 0
print (frase[9:]) # Se não sabe o número do caractere final, omita
